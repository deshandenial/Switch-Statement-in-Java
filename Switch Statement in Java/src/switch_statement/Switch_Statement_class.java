package switch_statement;



public class Switch_Statement_class 
{
	public static void main(String[] args) 
	{
		
		// initialize the switch statement in a block
		
		String name="joseph";
		
		switch(name)
		{
		case "bevin": 
		 
			System.out.println("print the bevin");
			break;
		
		case "deshan":
			System.out.println("print the deshan");
			break;
			
		case "joseph":
			System.out.println("print the joseph");
			break;
			
		default:
			
			System.out.println("invalid name form a list");
		}
			
	}

}
